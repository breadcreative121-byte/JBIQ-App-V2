function buildSystemPrompt(userProfile) {
  let out = SYSTEM_PROMPT;
  if (userProfile && userProfile.city) {
    const loc = [userProfile.city, userProfile.country].filter(Boolean).join(', ');
    out += `\n\nUser location: ${loc}. This is already known — NEVER ask where they are. Ground all "near me", weather, travel, and places questions in ${loc}.`;
  }
  return out;
}

